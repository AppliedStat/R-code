y <- c(32.00692, 37.75687, 43.84736, 46.26761, 46.90651,
       47.26220, 47.28952, 47.59391, 48.06508, 49.25429,
       50.27790, 50.48675, 50.66167, 53.33585, 53.49258,
       53.56681, 53.98112, 54.94154)
R <- c(54.94154, 54.94154)
#----------------------------------------------------------------------
## para <- c(49.56095,  4.81270)  ## Balakrishnan's solution
## para <- c(49.76609,  4.68761)  ## MLE
## para <- c(49.766095, 4.688264) ## MCEM
#----------------------


set.seed(1)
start0 = c(0,1)
MCEM = rc.dexp.MCEM(y,R,start=start0, maxits=10, eps=0, K=1000 )
QEM  = rc.dexp.QEM (y,R,start=start0, maxits=10, eps=0, K=1000 )

out = round(cbind(MCEM$out[,1],QEM$out[,1], MCEM$out[,2],QEM$out[,2] ),6)

colnames(out) = c("mu:MCEM", "mu:QEM",  "sigma:MCEM", "sigma:QEM") 



OUT = rbind( c(0,0,1,1), out )
rownames(OUT) = 0:10

cat("\n\n ********************* \n\n\n")
OUT

