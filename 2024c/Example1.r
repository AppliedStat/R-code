#============================================================================
data.b = c(550, 950, 1150, 1150, 1250, 1250, 1450, 1450, 1550, 2050, 3150)
data.w = c(750, 950, 1150, 1150, 1150, 1350, 1450, 1550, 1550, 1850)
##==========================================================================
idx  = c( rep(1,length(data.b)), rep(2,length(data.w)) )

data.all = c(data.b, data.w) / 1000   

idx.sort = order( data.all )
idx.b = ( idx[idx.sort]==1)
idx.w = ( idx[idx.sort]==2)
data.sort = data.all[idx.sort]
Fall     = ppoints( data.all )

##-----------------------------------------------------------------------
 X = sort(data.all) ; d = idx[ order(data.all) ]  ### NOTE: strength in grams.
##-----------------------------------------------------------------------


##-----------------------------------------------------------------------
## Exponential Model
##-----------------------------------------------------------------------
  cat("\n==================\n")
  cat(" Exponential Model")
  cat("\n==================\n")
  expo.cm.EM(X,d, eps=1.0E-5)

##-----------------------------------------------------------------------
## Weibull Model
##-----------------------------------------------------------------------
  cat("\n==================\n")
  cat(" Weibull Model")
  cat("\n==================\n")
  weibull.cm.EM(X,d, eps=1.0E-5)

##-----------------------------------------------------------------------
## Log-normal Model
##-----------------------------------------------------------------------
  cat("\n==================\n")
  cat(" Log-normal Model")
  cat("\n==================\n")
  norm.cm.EM(log(X),d, eps=1.0E-5)

##-----------------------------------------------------------------------
## Wald Model 
##-----------------------------------------------------------------------
  cat("\n==================\n")
  cat(" Wald Model")
  cat("\n==================\n")
  wald.cm.EM(X,d, eps=1.0E-5)

##-----------------------------------------------------------------------
## Inverse Weibull Model 
##-----------------------------------------------------------------------
  cat("\n==================\n")
  cat(" Inverse Weibull Model")
  cat("\n==================\n")
  invweibull.cr.EM(X,d, eps=1.0E-5)


