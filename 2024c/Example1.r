#============================================================================
data.b = c(550, 950, 1150, 1150, 1250, 1250, 1450, 1450, 1550, 2050, 3150)
data.w = c(750, 950, 1150, 1150, 1150, 1350, 1450, 1550, 1550, 1850)
##==========================================================================
idx  = c( rep(1,length(data.b)), rep(2,length(data.w)) )

data.all = c(data.b, data.w) / 1000   
X = sort(data.all) ; d = idx[ order(data.all) ]  ### NOTE: strength in grams.
##-----------------------------------------------------------------------

##-----------------------------------------------------------------------
## Weibull Model
##-----------------------------------------------------------------------
  cat("\n==================\n")
  cat(" Weibull Model")
  cat("\n==================\n")
  weibull.cm.EM(X,d, eps=1.0E-5)

##-----------------------------------------------------------------------
## Inverse Weibull Model 
##-----------------------------------------------------------------------
  cat("\n==================\n")
  cat(" Inverse Weibull Model")
  cat("\n==================\n")
  invweibull.cr.EM(X,d, eps=1.0E-5)


